    public void iniciaFormulario() {
//        jLabel_Data_menu.setText(menu.jLabelData.getText());
        if (jLabel_Id.getText() == "novo") {
            jLabel_Data_ent.setText(Principal.jLabel_Data.getText() + " " + Principal.jLabel_Hora.getText());
            jTextFieldNome.setText("");
            jTextFieldDescricao.setText("");
            jFormattedTextFieldCnpj.setText("");
            jFormattedTextFieldTelefone.setText("");
            jTextArea_Observacao.setText("");
            jTextFieldEndereco.setText("");
            jTextFieldNumero.setText("");
            jFormattedTextFieldCep.setText("");
            jTextFieldComplemento.setText("");
            jTextFieldBairro.setText("");
            jTextFieldCidade.setText("");
            jTextFieldInscricao.setText("");
            jComboBoxFC.setSelectedItem("FORN/CLIE");
            jButton_Excluir.setEnabled(false);
//            LimparTabela();
            jTextFieldNome.requestFocus();
            TravaCampos();
            jButton_Novo.setEnabled(true);
            jButton_Salvar.setEnabled(false);
            jButton_Excluir.setEnabled(false);
            this.setTitle("Cadastro Geral [Cliente/Fornecedor/Empresa/Transportadora]");
        } else {
            conex.conexao();
            conex.executaSql("select * from ecft   where ecft_id = '" + jLabel_Id.getText() + "' order by ecft_id asc ");
            try {
                conex.rs.first();
//            jTextFieldCodCli.setText(String.valueOf(conex.rs.getInt("fc_id")));
                this.setTitle("Cadastro Geral [Cliente/Fornecedor/Empresa/Transportadora]    Id: " + (conex.rs.getString("sis_ecft")));
                jTextFieldNome.setText(conex.rs.getString("ecft_nome"));
                jTextFieldDescricao.setText(conex.rs.getString("ecft_descricao"));
                jComboBoxFC.setSelectedItem(conex.rs.getString("ecft_tipo"));
                jTextFieldInscricao.setText((conex.rs.getString("ecft_inscricao")));
                jTextFieldEndereco.setText(conex.rs.getString("ecft_endereco"));
                jTextFieldNumero.setText(conex.rs.getString("ecft_no"));
                jTextFieldComplemento.setText(conex.rs.getString("ecft_complemento"));
                jTextFieldBairro.setText(conex.rs.getString("ecft_bairro"));
                jTextFieldCidade.setText(conex.rs.getString("ecft_cidade"));
                jTextArea_Observacao.setText(conex.rs.getString("ecft_observacao"));
                // jLabel_status.setText((conex.rs.getString("ecft_status")));
                jLabel_Data_ent.setText((conex.rs.getString("ecft_registro")));
                id_referencia = ((conex.rs.getInt("sis_ecft")));
                cep = (conex.rs.getString("ecft_cep"));
                if (cep == ("  .   -   ")) {
                    jFormattedTextFieldCep.setText(null);
                } else {
                    jFormattedTextFieldCep.setText(cep);
                }
                cnpj = (conex.rs.getString("ecft_cnpj"));
                if (cnpj == ("  .   .   /    -  ")) {
                    jFormattedTextFieldCnpj.setText(null);
                } else {
                    jFormattedTextFieldCnpj.setText(cnpj);
                }
                telefone = (conex.rs.getString("ecft_telefone"));
                if (telefone == ("(  )    -    ")) {
                    jFormattedTextFieldTelefone.setText(null);
                } else {
                    jFormattedTextFieldTelefone.setText(telefone);
                }
                jButton_Novo.setEnabled(false);
                jButton_Salvar.setEnabled(true);
                jButton_Excluir.setEnabled(true);
                LiberaCampos();

            } catch (SQLException ex) {
//                JOptionPane.showMessageDialog(rootPane, "Erro ao iniciar formulario. \n" + ex);
                LimpaCampos();
                jButton_Novo.setEnabled(true);
                jButton_Salvar.setEnabled(false);
                jButton_Excluir.setEnabled(false);
            }
            conex.desconecta();

        }

    }

