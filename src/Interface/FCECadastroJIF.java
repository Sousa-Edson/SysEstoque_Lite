    public void inserirDados() {

        BEANS.setEcft_nome(jTextFieldNome.getText());

        /////   .   .   /    -  
        if (jFormattedTextFieldCnpj.getText().equals("  .   .   /    -  ")) {
            BEANS.setEcft_cnpj("");
        } else {
            BEANS.setEcft_cnpj(jFormattedTextFieldCnpj.getText());
        }
        if (jFormattedTextFieldTelefone.getText().equals("(  )    -    ")) {
            BEANS.setEcft_telefone("");
        } else {
            BEANS.setEcft_telefone(jFormattedTextFieldTelefone.getText());
        }
        if (jFormattedTextFieldCep.getText().equals("  .   -   ")) {
            BEANS.setEcft_cep("");
        } else {
            BEANS.setEcft_cep(jFormattedTextFieldCep.getText());
        }
        BEANS.setEcft_descricao(jTextFieldDescricao.getText());
        BEANS.setEcft_tipo((String) jComboBoxFC.getSelectedItem());
        BEANS.setEcft_endereco(jTextFieldEndereco.getText());
        BEANS.setEcft_no(jTextFieldNumero.getText());
        BEANS.setEcft_complemento(jTextFieldComplemento.getText());
        BEANS.setEcft_bairro(jTextFieldBairro.getText());
        BEANS.setEcft_cidade(jTextFieldCidade.getText());
        BEANS.setEcft_inscricao(jTextFieldInscricao.getText());
        BEANS.setEcft_observacao(jTextArea_Observacao.getText());
        BEANS.setSis_EFCT(id_referencia);// aqui puxa referencia
        BEANS.setEcft_usuario(Principal.jLabelNomeUsuario.getText());
//        BEANS.setEcft_status(jLabel_status.getText());
        BEANS.setStecft(1);
        BEANS.setEcft_registro(Principal.jLabel_Data.getText()+" "+Principal.jLabel_Hora.getText());
    }


