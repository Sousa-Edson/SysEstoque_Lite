    public void PreencheUnidade() {
      ArrayList  lista = new ArrayList();
        conex.conexao();
        conex.executaSql2("SELECT ecft_id, sis_ecft, ecft_tipo, ecft_nome, ecft_cnpj, ecft_inscricao, \n"
                + "       ecft_descricao, ecft_telefone, ecft_endereco, ecft_no, ecft_cep, \n"
                + "       ecft_complemento, ecft_bairro, ecft_cidade, ecft_observacao, \n"
                + "       ecft_usuario, ecft_registro, stecft\n"
                + "  FROM ecft where stecft=1 and ecft_tipo ='TRANSPORTE' ");
        try {
            conex.rs.first();
            jComboBox_Unidade.removeAllItems();
            jComboBox_Unidade1.removeAllItems();
            jComboBox1.removeAllItems();
//lista=(conex.rs.getString("ecft_nome"));
            do {
                
                String unidade = ((conex.rs.getString("ecft_nome")));
                String unidade1 = (conex.rs.getString("sis_ecft"));
                jComboBox_Unidade.addItem(unidade);
                jComboBox_Unidade1.addItem(unidade1);
                jLabel3.setText(unidade1);
//                jComboBox1.addItem(conex.rs.getString("sigla_unidade") + conex.rs.getString("id_referenciaunidade"));
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            Logger.getLogger(ProdCadastroJIF.class.getName()).log(Level.SEVERE, null, ex);
//            jComboBox_Unidade.addItem("UN");
//            jComboBox_Unidade.setSelectedItem("UN");
        }
        conex.desconecta();
    }

    public void BuscaPreencheUnidade() {
        MostraTabela = "" + jComboBox_Unidade.getSelectedItem();
        conex2.conexao();
        conex2.executaSql2("SELECT id_unidade, id_referenciaunidade, sigla_unidade, desc_unidade, \n"
                + "       registro_unidade, usuario_unidade, stunid FROM unidade "
                + " where stunid=1 and sigla_unidade like'%" + MostraTabela + "%'   order by id_referenciaunidade asc ");
        try {
            conex2.rs.first();
            jComboBox_Unidade1.removeAllItems();
            do {
//                String unidade = (conex.rs.getString("id_referencia")+" "+(conex.rs.getString("sigla_unidade")));
                String unidade1 = (conex2.rs.getString("id_referenciaunidade"));
//                jComboBox_Unidade.addItem(unidade);
                jComboBox_Unidade1.addItem(unidade1);
                jLabel1.setText(unidade1);
            } while (conex2.rs.next());
        } catch (SQLException ex) {
            Logger.getLogger(ProdCadastroJIF.class.getName()).log(Level.SEVERE, null, ex);
            jComboBox_Unidade.addItem("UN");
            jComboBox_Unidade.setSelectedItem("UN");
        }
        conex2.desconecta();
    }
