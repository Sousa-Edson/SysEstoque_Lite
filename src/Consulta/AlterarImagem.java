    private void btnImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImagemActionPerformed
         String LocalImagem = "C:\\Users\\edson\\Pictures";
        LocalImagem = LocalImagem.replace("\\", "/");
        JFileChooser fc = new JFileChooser();
          fc = new JFileChooser(new File(LocalImagem));
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
                "JPG & GIF Images", "jpg", "gif");  //Cria um filtro
        fc.setFileFilter(filter);  //Altera o filtro do JFileChooser
        int res = fc.showOpenDialog(null);

        if (res == JFileChooser.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();

            try {
                imagem = ManipularImagem.setImagemDimensao(arquivo.getAbsolutePath(), 260, 260);
                imagem2 = ManipularImagem.setImagemDimensao(arquivo.getAbsolutePath(), 560, 560);
                lblImagem.setIcon(new ImageIcon(imagem));

            } catch (Exception ex) {
                // System.out.println(ex.printStackTrace().toString());
            }

        } else {
            JOptionPane.showMessageDialog(null, "Voce nao selecionou nenhum arquivo.");
        }
    }//GEN-LAST:event_btnImagemActionPerformed

    private void blnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blnEnviarActionPerformed
        String s = jLabel_Id_Imagem.getText();
        int id = Integer.parseInt(s);
//        JOptionPane.showMessageDialog(rootPane, id);
        try {
            Imagem_Beans obj2 = new Imagem_Beans();
            obj2.setProduto(id);
            Imagem_Beans obj = new Imagem_Beans();
            obj.setImagem(ManipularImagem.getImgBytes(imagem2));
            Imagem_DAO dao = new Imagem_DAO();
//             JOptionPane.showMessageDialog(rootPane, obj2.getProduto());
            Boolean foi = dao.alterar(obj, obj2);
            if (foi) {
                JOptionPane.showMessageDialog(rootPane, "Imagem alterada com sucesso");
                FrameExibirImagem.jButton1.doClick();
                this.lblImagem.setIcon(null);
                this.dispose();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Imagem n√£o alterada");

            }

        } catch (Exception ex) {
            Logger.getLogger(AlterarImagem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_blnEnviarActionPerformed

