    public void preencherTabela() {
        lblImg.setIcon(null);
        String busca = jLabel_Id_Produto.getText();
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Registro", ""};
        conex.conexao();

        conex.executaSql2("SELECT id, imagem, produtoid FROM imagemproduto  where produtoid= '" + busca + "' order by id asc;");
        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("id"), conex.rs.getInt("produtoid")});

            } while (conex.rs.next());
        } catch (SQLException ex) {
            System.out.println("Consulta.FrameExibirImagem.preencherTabela()  " + ex);
//            JOptionPane.showMessageDialog(rootPane, "" + ex);
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTable_Produto.setModel(modelo);
        jTable_Produto.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTable_Produto.getColumnModel().getColumn(0).setResizable(false);
        jTable_Produto.getColumnModel().getColumn(1).setPreferredWidth(20);
        jTable_Produto.getColumnModel().getColumn(1).setResizable(false);

        conex.desconecta();

    }

    public void LimpaTabela() {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Registro"};
        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTable_Produto.setModel(modelo);
    }

